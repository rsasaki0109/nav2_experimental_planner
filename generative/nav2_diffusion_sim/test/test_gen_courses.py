def test_committed_artifacts_match_the_specs():
    # The worlds/ and maps/ checked into the package must equal a fresh render,
    # so a spec edit without re-running gen_courses.main() fails CI.
    pkg = os.path.join(_HERE, '..')
    for name in gen.COURSE_SPECS:
        world_path = os.path.join(pkg, 'worlds', name + '.sdf.xacro')
        with open(world_path) as fh:
            assert fh.read() == gen.world_sdf(name), \
                'worlds/{}.sdf.xacro is stale; re-run gen_courses.main()'.format(name)
        pgm, yaml, _ = gen.occupancy_map(name)
        with open(os.path.join(pkg, 'maps', name + '.yaml')) as fh:
            assert fh.read() == yaml, \
                'maps/{}.yaml is stale; re-run gen_courses.main()'.format(name)
        with open(os.path.join(pkg, 'maps', name + '.pgm'), 'rb') as fh:
            assert fh.read() == pgm, \
                'maps/{}.pgm is stale; re-run gen_courses.main()'.format(name)
